			<h2>Concept</h2>
			<p>
				<strong>Plus d'une vingtaine d'entreprises</strong> est invitée au sein du département afin de venir à la <strong>rencontre des étudiants</strong>. Celles-ci y sont accueillies par une équipe composée exclusivement d'étudiants, et la journée, banalisée pour l'évènement, permettra à chacun de venir se renseigner, poser des questions, prendre des contacts, ...
			</p>
			<p>
				En fin de matinée, l'équipe organisatrice et les intervenants de chaque entreprise seront conviés à un déjeuner de manière à ponctuer l'évènement par une pause conviviale.
			</p>
			<p>
				Les Rencontres IF sont aussi une occasion idéale pour les étudiants de <strong>trouver un stage ou un éventuel futur premier emploi</strong>. Il est ainsi conseillé aux entreprises de préparer, si elles le désirent, plusieurs propositions adaptées aux différentes promotions.
			</p>
			<p>
				Encore une fois, l'objectif de cet évènement est évidemment d'offrir une <span class="hero_motcle">visibilité unique</span> entre étudiants et entreprises, et de tisser des <strong>relations</strong> entre les deux.

<h1>Présentation</h1>
<p>
	Les "Rencontres IF" (ou "RIFs") sont une manifestation propre au Département Informatique de l'INSA de Lyon. Elles permettent, le temps d'une journée, d'élaborer un lien privilégié entre les entreprises et les étudiants.<br />Fort de son succès depuis plus de <strong>10 ans</strong> cet évènement se renouvelle chaque année en recherchant toujours plus de qualité dans sa prestation.
</p>
<h1>Concept</h1>
<p>
	Une <strong>vingtaine d'entreprises</strong> est invitée au sein du département afin de venir à la <strong>rencontre des étudiants</strong>. Celles-ci y sont accueillies par une équipe composée exclusivement d'étudiants, et la journée, banalisée pour l'évènement, permettra à chacun de venir se renseigner, poser des questions, prendre des contacts, ...<br />
</p>
